// 5.- Escribe un programa de tres líneas que pida un número, pida otro número y escriba el resultado de sumar estos dos números.
// Nota: Tener en cuenta la siguiente función: parseInt

const numero1 = parseInt(prompt('Ingrese primer número a sumar: '))
const numero2 = parseInt(prompt('Ingrese segundo número a sumar: '))
document.writeln('La suma de ambos números es = ',numero1+numero2)